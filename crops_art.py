SPRITES = {}


def sprite(name, rows):
    assert len(rows) == 16, f"{name}: {len(rows)} rows"
    for i, row in enumerate(rows):
        assert len(row) == 16, f"{name} row {i}: {len(row)} chars"
        for ch in row:
            assert ch in PALETTE, f"{name} row {i}: unknown char {ch!r}"
    SPRITES[name] = rows


